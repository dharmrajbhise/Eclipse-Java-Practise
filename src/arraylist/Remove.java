package arraylist;

import java.util.ArrayList;

public class Remove {

	public static void main(String[] args) {
		
				ArrayList<String> color = new java.util.ArrayList<>();
						
						color.add("Red");
						color.add("Yellow");
						color.add("Green");
						color.add("Pink");
						color.add("black");
						color.add("brown");
						color.add("Grey");
						
						System.out.println("Before removal: "+color);
						color.remove(1);
						color.remove(3);
						
						
						System.out.println("After removal : "+color);
						
						
						
						

	}

}
